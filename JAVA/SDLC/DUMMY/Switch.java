package com.ust;

public class Switch {
 public static void main(String args[]) {
	 String str="Green";
	 switch(str) {
	 case"Red":
		 System.out.println("I am in Red");
		 System.out.println("**");
		 break;
		 
	 default:
		 System.out.println("default");
		 System.out.println("**");
		 
	 case"Green":
		 System.out.println("I am in Green");
		 System.out.println("**");
		 break;
		 
	 case"Blue":
		 System.out.println("I am in Blue");
		 System.out.println("**");
		 
		  }
 }
}
